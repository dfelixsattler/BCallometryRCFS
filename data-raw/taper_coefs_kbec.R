#
# Builds the taper_coefs_kbec package dataset.
#
# Source:
#   Kozak, A. (2004). My last words on taper equations. The Forestry Chronicle,
#   80(4), 507-515.
#
#   Coefficients and species-BEC lookup table transcribed from:
#   Luo, Y. (FAIBBase R package, bcgov/FAIBBase), R/DIB_ICalculator.R
#   which implements the BC Ministry of Forests KBEC 2002 taper equations
#   (equivalent to the vol_tree_active_equation subroutine in vol_setup macro).
#
# The dataset is a 208-row data frame (16 BC species × 13 BEC zones) with
# pre-joined Kozak (2002) taper coefficients B1-B9 and ERR.
#
# Kozak (2002) KBEC variable-exponent taper equation:
#   dib = ERR * B1 * DBH^B2 * H^B3 * X^(B4*Z^4 + B5/exp(D/H) +
#                                          B6*X^0.1 + B7/DBH +
#                                          B8*H^Q + B9*X)
# where:
#   Z = h_i / H                  (relative height)
#   P = 1.3 / H                  (relative breast height)
#   Q = 1 - Z^(1/3)
#   X = Q / (1 - P^(1/3))

