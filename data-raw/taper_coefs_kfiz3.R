# Builds the taper_coefs_kfiz3 package dataset.
#
# Source:
#   Coefficients transcribed from the BC Ministry of Forests volume
#   compilation system (KFIZ3 form, A_FIZ array in the original SAS macro),
#   as reproduced in:
#   Luo, Y. (FAIBBase R package, bcgov/FAIBBase), R/DIB_ICalculator.R,
#   function taperCoeffsGenerator(), KFIZ3 branch.
#
# The dataset is a 192-row data frame (16 BC species x 12 FIZ zones) with
# pre-joined Kozak variable-exponent taper coefficients P, A0-A7.
#
# KFIZ3 variable-exponent taper equation (Kozak, sqrt-transform form):
#   dib = A0 * DBH^A1 * A2^DBH * X^EX
# where:
#   Z  = h_i / H                               (relative height)
#   X  = (1 - sqrt(Z)) / (1 - sqrt(P))        (shape variable; P is fitted)
#   EX = A3*Z^2 + A4*log(Z+0.001)
#      + A5*sqrt(X) + A6*(DBH/H) + A7*exp(Z)
#
# FIZ zones are grouped into three equation groups:
#   Group 1 (FIZ A, B, C)               -- Interior/Northern BC
#   Group 2 (FIZ D, E, F, G, H, I, J)  -- Central/Southern BC
#   Group 3 (FIZ K, L)                  -- Coastal BC

